CREATE PROCEDURE [cass].[p_Добавить_информацию_об_ошибке_обмена]
	@Код_кассы int, @Сообщение nvarchar(max) = 'No message'
AS
	Insert into 
		[cass].t_j_Ошибки_опроса_касс (Дата_время_события, Код_кассы, Сообщение)
	values 
		(getDate(), @Код_кассы, @Сообщение)
RETURN 0
